package com.redhat.devtools.intellij.lsp4ij.operations.completion.snippet.handler;

public class TabstopNode implements LspSnippetNode {

    private final int index;

    public TabstopNode(int index) {
        this.index = index;
    }

    public int getIndex() {
        return index;
    }

    @Override
    public String toString() {
        return "tabstop(" + index + ")";
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + index;
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        TabstopNode other = (TabstopNode) obj;
        if (index != other.index)
            return false;
        return true;
    }

}
