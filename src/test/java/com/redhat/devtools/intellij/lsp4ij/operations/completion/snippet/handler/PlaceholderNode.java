package com.redhat.devtools.intellij.lsp4ij.operations.completion.snippet.handler;

public class PlaceholderNode implements LspSnippetNode {

    private final int index;
    private final String name;
    private final int level;

    public PlaceholderNode(int index, String name, int level) {
        this.index = index;
        this.name = name;
        this.level = level;
    }

    public int getIndex() {
        return index;
    }

    public String getName() {
        return name;
    }

    public int getLevel() {
        return level;
    }

    @Override
    public String toString() {
        return "placeholder(" + index + "," + name + "," + level + ")";
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + index;
        result = prime * result + level;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        PlaceholderNode other = (PlaceholderNode) obj;
        if (index != other.index)
            return false;
        if (level != other.level)
            return false;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        return true;
    }

}
